public class UpdatingVariables {
    public static void main(String[] args) {
        int salary = 1000;

        // Bono $200
        salary += 200;
        System.out.println(salary);

        String employeeName = "Anahi Salgado";
        employeeName += " Diaz de la Vega";
        System.out.println(employeeName);
    }
}
